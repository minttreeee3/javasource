package ch3;


public class IfEx2 {
	public static void main(String[] args) {


		int jum =45;
		char grade = ' ';
		
		if (jum>=90) { 
			grade='A';
		}else if (jum>=80) {
			grade='B';
		} else if (jum>=70) {
			grade='C';
		}else if (jum>=60) {
			grade='D';
		}else if (jum>=50) {
			grade='E';
		}else  {
			grade='F';}
		System.out.printf("점수 : %d, 등급 :%c\n",jum,grade);
		
		
		
		
		
		
		
		
	}
}
		
		
		
		
		
		
		
		
		
		
		
		
		
		

